import math
print(math.pi) # nilai pi
print(math.fabs(-4.7)) # nilai absolute
print(math.pow(10, 3)) # nilai 10 pangkat 3 (nilainya lebih absolut untuk membuat pangkat)
print(math.sqrt(64)) # nilai akar kuadrat
print(math.cos(math.pi))

print(round(5.7)) # pembulatan
print(round(5.2)) # pembulatan
print(math.floor(7.7)) # pembulatan ke bawah
print(math.ceil(6.01)) # pembulatan ke atas